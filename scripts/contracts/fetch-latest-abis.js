import { createPublicClient, http, defineChain, getAddress } from "viem";
import { writeFile } from "node:fs/promises";
import { resolve } from "node:path";
import { execSync } from "node:child_process";
import process from "node:process";

const CHAINS = {
  1: {
    name: "Ethereum",
    rpc: "https://ethereum-rpc.publicnode.com/d8a2cc6e7483872e917d7899f9403d738b001c80e37d66834f4e40e9efb54a27",
    etherscanApi: "https://api.etherscan.io/v2/api",
    abiDir: resolve("src/lib/abi/mainnet/v4"),
  },
  560048: {
    name: "Hoodi",
    rpc: "https://ethereum-hoodi-rpc.publicnode.com/d8a2cc6e7483872e917d7899f9403d738b001c80e37d66834f4e40e9efb54a27",
    etherscanApi: "https://api.etherscan.io/v2/api",
    abiDir: resolve("src/lib/abi/mainnet/v4"),
  },
};

const [, , setterAddress, getterAddress, chainIdArg, apiKeyArg] = process.argv;

if (!setterAddress || !getterAddress) {
  console.error(
    "Usage: node scripts/contracts/fetch-latest-abis.js <setterAddress> <getterAddress> [chainId=560048] [etherscanApiKey]",
  );
  process.exit(1);
}

const chainId = Number(chainIdArg ?? 560048);
const apiKey = apiKeyArg || "7D5ZWV94V4N8G13GXI2PWGM8PU8G7NY366";

const chainConfig = CHAINS[chainId];
if (!chainConfig) {
  console.error(`Unsupported chain ID: ${chainId}. Supported: 1, 560048`);
  process.exit(1);
}

const chain = defineChain({
  id: chainId,
  name: chainConfig.name,
  nativeCurrency: { name: "Ether", symbol: "ETH", decimals: 18 },
  rpcUrls: { default: { http: [chainConfig.rpc] } },
});

const client = createPublicClient({
  chain,
  transport: http(chainConfig.rpc),
});

const EIP1967_SLOT =
  "0x360894a13ba1a3210667c828492db98dca3e2076cc3735a920a3ca505d382bbc";

async function getImplementation(proxyAddress) {
  const slot = await client.getStorageAt({
    address: proxyAddress,
    slot: EIP1967_SLOT,
  });
  if (!slot || slot === "0x" + "0".repeat(64)) {
    return proxyAddress;
  }
  return getAddress("0x" + slot.slice(-40));
}

async function fetchAbi(address) {
  const url = `${chainConfig.etherscanApi}?apikey=${apiKey}&chainid=${chainId}&module=contract&action=getabi&address=${address}`;
  const res = await fetch(url);
  const json = await res.json();
  if (json.status !== "1") {
    throw new Error(`Etherscan error: ${json.message} — ${json.result}`);
  }
  return JSON.parse(json.result);
}

const ABIS = [
  {
    name: "SSVNetwork (setter)",
    proxy: setterAddress,
    jsonFile: "setter.json",
    tsFile: "setter.ts",
    tsExport: "MainnetV4SetterABI",
  },
  {
    name: "SSVNetworkViews (getter)",
    proxy: getterAddress,
    jsonFile: "getter.json",
    tsFile: "getter.ts",
    tsExport: "MainnetV4GetterABI",
  },
];

const GENERATED_COMMENT = `// This file is auto-generated by scripts/contracts/update-abi.js — do not edit manually.\n`;

function abiToTs(exportName, abi) {
  const body = JSON.stringify(abi, null, 2)
    .slice(1, -1)
    .replace(/"([^"]+)":/g, "$1:");
  return `${GENERATED_COMMENT}export const ${exportName} = [${body}\n] as const;\n`;
}

for (const { name, proxy, jsonFile, tsFile, tsExport } of ABIS) {
  console.log(`\n${name}:`);

  const impl = await getImplementation(proxy);
  console.log(`  Proxy:          ${proxy}`);
  console.log(`  Implementation: ${impl}`);

  let abi;
  try {
    abi = await fetchAbi(impl);
  } catch (e) {
    console.error(`  Failed to fetch ABI: ${e.message}`);
    process.exit(1);
  }

  const jsonPath = resolve(chainConfig.abiDir, jsonFile);
  await writeFile(jsonPath, JSON.stringify(abi, null, 2) + "\n");
  console.log(`  Updated JSON: ${jsonPath} (${abi.length} items)`);

  const tsPath = resolve(chainConfig.abiDir, tsFile);
  await writeFile(tsPath, abiToTs(tsExport, abi));
  console.log(`  Updated TS:   ${tsPath}`);

  execSync(`pnpm prettier --write "${jsonPath}" "${tsPath}"`, {
    stdio: "inherit",
  });
  console.log(`  Formatted:    ${jsonFile}, ${tsFile}`);
}

console.log("\nDone.");
